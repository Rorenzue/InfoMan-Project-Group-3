        // Slider functionality
        const dots = document.querySelectorAll('.dot');
        let currentSlide = 0;
        
        dots.forEach((dot, index) => {
            dot.addEventListener('click', () => {
                setActiveSlide(index);
            });
        });
        
        function setActiveSlide(index) {
            dots.forEach(dot => dot.classList.remove('active'));
            dots[index].classList.add('active');
            currentSlide = index;
            // Here you would normally change the slide content
        }
        
        // Navigation smooth scroll
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function(e) {
                e.preventDefault();
                
                const targetId = this.getAttribute('href');
                if (targetId === '#') return;
                
                const targetElement = document.querySelector(targetId);
                if (targetElement) {
                    window.scrollTo({
                        top: targetElement.offsetTop,
                        behavior: 'smooth'
                    });
                }
            });
        });
        
        // Process steps interaction
        const processSteps = document.querySelectorAll('.process-step');
        const processLines = document.querySelectorAll('.process-line');
        
        processSteps.forEach((step, index) => {
            step.addEventListener('click', () => {
                // Reset all steps
                processSteps.forEach(s => {
                    s.querySelector('.step-circle').classList.remove('active');
                    s.querySelector('.step-text').classList.remove('active');
                });
                
                // Reset all lines
                processLines.forEach(line => line.classList.remove('active'));
                
                // Set active state for current and previous steps
                for (let i = 0; i <= index; i++) {
                    processSteps[i].querySelector('.step-circle').classList.add('active');
                    processSteps[i].querySelector('.step-text').classList.add('active');
                    
                    // Set active state for lines before the current step
                    if (i < index) {
                        processLines[i].classList.add('active');
                    }
                }
                
                // Update process content based on selected step
                updateProcessContent(index);
            });
        });
        
        function updateProcessContent(stepIndex) {
            const processContent = document.querySelector('.process-content');
            const contents = [
                '<h3>Step 1: Register</h3><p>Create an account on the NCIP portal to start your application process.</p>',
                '<h3>Step 2: Access</h3><p>Log in to your account and access the scholarship application form.</p>',
                '<h3>Step 3: Submission</h3><p>Complete all required fields and upload the necessary documents.</p>',
                '<h3>Step 4: Evaluation</h3><p>Wait for the evaluation of your application by the NCIP committee.</p>'
            ];
            
            processContent.innerHTML = contents[stepIndex];
        }
        
        // Initialize with first step content
        updateProcessContent(0);
    </script>
